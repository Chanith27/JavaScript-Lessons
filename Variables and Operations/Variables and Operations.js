var name = "Chanith";   // using 'var' (older way)
let age = 23;           // using 'let' (modern)
const country = "Sri Lanka";  // constant value

console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);

 // Basic Data Types
    let isStudent = true;
    let height = 5.9;
    let score = null;
    let undefinedVar;

    console.log("Is Student?", isStudent);
    console.log("Height:", height);
    console.log("Score:", score);
    console.log("Undefined Variable:", undefinedVar);

// Basic Arithmetic Operations
    let x = 10;
    let y = 4;

    console.log("Addition:", x + y);         // 14
    console.log("Subtraction:", x - y);      // 6
    console.log("Multiplication:", x * y);   // 40
    console.log("Division:", x / y);         // 2.5
    console.log("Modulus (Remainder):", x % y);  // 2

    // String Operations
    let firstName = "Chanith";
    let lastName = "Adikari";
    let fullName = firstName + " " + lastName;

    console.log("Full Name:", fullName);

 // Updating Variables
    let counter = 0;
    counter += 1;  // same as counter = counter + 1
    counter++;     // increment by 1
    counter--;     // decrement by 1

    console.log("Counter:", counter);
